// TestIntegrationEndToEnd compiles an empty target, plans, verifies,
// and explains. It exercises the full happy path of every public
// entry point.
func TestIntegrationEndToEnd(t *testing.T) {
	withCompilerVersion(t, "0.1.0")
	pack, err := LoadCorePack()
	if err != nil {
		t.Fatalf("LoadCorePack: %v", err)
	}
	prof, err := pack.MustProfile(ProfileId("fsharp-elm-service-v1"))
	if err != nil {
		t.Fatalf("MustProfile: %v", err)
	}
	target := newEmptyTarget(t)
	plan, err := Plan(pack, prof, target)
	if err != nil {
		t.Fatalf("Plan: %v", err)
	}
	if len(plan.Actions) == 0 {
		t.Fatalf("Plan produced no actions")
	}
	if _, err := Compile(pack, prof, target, CompilerOptions{}); err != nil {
		t.Fatalf("Compile: %v", err)
	}
	rep, err := Explain(pack, prof, target, "dev", "unknown", "unknown")
	if err != nil {
		t.Fatalf("Explain: %v", err)
	}
	if len(rep.Doctrines) == 0 {
		t.Errorf("Explain produced no doctrines")
	}
	result, err := Verify(pack, prof, target)
	if err != nil {
		t.Fatalf("Verify: %v", err)
	}
	if !result.OK {
		t.Errorf("Verify failed: %v", result.Findings)
	}
}

// TestIntegrationGoldenProjection is the headline integration test: it
// compiles an empty target and asserts every projected file matches
// the golden fixture byte-for-byte.
func TestIntegrationGoldenProjection(t *testing.T) {
	pack, err := LoadCorePack()
	if err != nil {
		t.Fatalf("LoadCorePack: %v", err)
	}
	prof, err := pack.MustProfile(ProfileId("fsharp-elm-service-v1"))
	if err != nil {
		t.Fatalf("MustProfile: %v", err)
	}
	target := newEmptyTarget(t)
	if _, err := Compile(pack, prof, target, CompilerOptions{}); err != nil {
		t.Fatalf("Compile: %v", err)
	}
	gotTree := listTargetTree(t, target)
	wantTree := []string{
		".factory/doctrine.lock.json",
		".factory/generated/doctrine-inventory.md",
		".factory/generated/factory.mk",
		".factory/project.json",
		"Makefile",
		"docs/factory/README.md",
	}
	if !equalStringSet(gotTree, wantTree) {
		t.Fatalf("tree mismatch: got %v want %v", gotTree, wantTree)
	}
	for _, p := range wantTree {
		got := mustRead(t, filepath.Join(target, p))
		golden := mustRead(t, filepath.Join(expectedFixtureDir(t), p))
		if string(got) != string(golden) {
			t.Errorf("file %s differs from golden", p)
		}
	}
}

