// TestIntegrationIdempotent compiles twice and asserts the second
// run touches no bytes in the managed tree.
func TestIntegrationIdempotent(t *testing.T) {
	pack, err := LoadCorePack()
	if err != nil {
		t.Fatalf("LoadCorePack: %v", err)
	}
	prof, err := pack.MustProfile(ProfileId("fsharp-elm-service-v1"))
	if err != nil {
		t.Fatalf("MustProfile: %v", err)
	}
	target := newEmptyTarget(t)
	if _, err := Compile(pack, prof, target, CompilerOptions{}); err != nil {
		t.Fatalf("Compile: %v", err)
	}
	before := make(map[string]string)
	for _, p := range listTargetTree(t, target) {
		before[p] = string(mustRead(t, filepath.Join(target, p)))
	}
	if _, err := Compile(pack, prof, target, CompilerOptions{}); err != nil {
		t.Fatalf("Compile: %v", err)
	}
	for p, content := range before {
		got, err := os.ReadFile(filepath.Join(target, p))
		if err != nil {
			t.Errorf("file %s vanished: %v", p, err)
			continue
		}
		if string(got) != content {
			t.Errorf("file %s changed in idempotent compile", p)
		}
	}
}

// TestIntegrationNoticeOnManagedFiles ensures every managed output
// carries the documented "Generated by Leamas" notice.
func TestIntegrationNoticeOnManagedFiles(t *testing.T) {
	pack, err := LoadCorePack()
	if err != nil {
		t.Fatalf("LoadCorePack: %v", err)
	}
	prof, err := pack.MustProfile(ProfileId("fsharp-elm-service-v1"))
	if err != nil {
		t.Fatalf("MustProfile: %v", err)
	}
	target := newEmptyTarget(t)
	if _, err := Compile(pack, prof, target, CompilerOptions{}); err != nil {
		t.Fatalf("Compile: %v", err)
	}
	managed := []string{
		".factory/generated/factory.mk",
		".factory/generated/doctrine-inventory.md",
		"docs/factory/README.md",
	}
	for _, p := range managed {
		data, err := os.ReadFile(filepath.Join(target, p))
		if err != nil {
			t.Fatalf("read %s: %v", p, err)
		}
		if !strings.Contains(string(data), "Generated by Leamas from factory-core-v1") {
			t.Errorf("missing generated notice in %s", p)
		}
		if !strings.Contains(string(data), "Do not edit this file directly") {
			t.Errorf("missing edit warning in %s", p)
		}
	}
}

// mustRead reads a file or fails the test.
func mustRead(t *testing.T, path string) []byte {
	t.Helper()
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read %s: %v", path, err)
	}
	return data
}
