func TestDefaultConfig(t *testing.T) {
	cfg := DefaultConfig()
	if cfg.MinLines != 40 {
		t.Errorf("expected MinLines=40, got %d", cfg.MinLines)
	}
	if cfg.MinTokens != 400 {
		t.Errorf("expected MinTokens=400, got %d", cfg.MinTokens)
	}
	if len(cfg.ExcludeDirs) == 0 {
		t.Error("expected ExcludeDirs to be non-empty")
	}
}

func TestIsGeneratedFile(t *testing.T) {
	tmpDir := t.TempDir()

	tests := []struct {
		name     string
		content  string
		expected bool
	}{
		{
			name:     "canonical marker",
			content:  "// Code generated by tool DO NOT EDIT.\npackage main\n",
			expected: true,
		},
		{
			name:     "canonical with different tool",
			content:  "// Code generated by protoc-gen-go DO NOT EDIT.\npackage main\n",
			expected: true,
		},
		{
			name:     "go generate is NOT generated",
			content:  "//go:generate something\npackage main\n",
			expected: false,
		},
		{
			name:     "normal file",
			content:  "package main\nfunc main() {}\n",
			expected: false,
		},
		{
			name:     "unrelated code generated text",
			content:  "package main\n// This code generates output\nfunc main() {}\n",
			expected: false,
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			path := filepath.Join(tmpDir, "test.go")
			if err := os.WriteFile(path, []byte(tc.content), 0644); err != nil {
				t.Fatalf("failed to write test file: %v", err)
			}
			result := isGeneratedFile(path)
			if result != tc.expected {
				t.Errorf("isGeneratedFile(%q) = %v, expected %v", tc.name, result, tc.expected)
			}
		})
	}
}

