func TestListGoFiles(t *testing.T) {
	tmpDir := t.TempDir()

	// Files we want to find
	wantFound := map[string]string{
		"main.go":         "package main\nfunc main() {}\n",
		"util.go":         "package main\nfunc util() {}\n",
		"subpkg/extra.go": "package subpkg\nfunc extra() {}\n",
		"normal_code.go":  "// Code generated tool output\npackage main\nfunc normal() {}\n",
	}

	// Files we want to exclude
	wantAbsent := map[string]string{
		"util_test.go":    "package main\nfunc TestUtil(t *testing.T) {}\n",
		"generated.pb.go": "package main\n// Code generated DO NOT EDIT.\nfunc generated() {}\n",
		"vendor/pkg.go":   "package vendor\nfunc vendor() {}\n",
	}

	for name, content := range wantFound {
		path := filepath.Join(tmpDir, name)
		if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
			t.Fatalf("failed to create dir for %s: %v", name, err)
		}
		if err := os.WriteFile(path, []byte(content), 0644); err != nil {
			t.Fatalf("failed to write %s: %v", name, err)
		}
	}

	for name, content := range wantAbsent {
		path := filepath.Join(tmpDir, name)
		if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
			t.Fatalf("failed to create dir for %s: %v", name, err)
		}
		if err := os.WriteFile(path, []byte(content), 0644); err != nil {
			t.Fatalf("failed to write %s: %v", name, err)
		}
	}

	cfg := DefaultConfig()

	found, err := listGoFiles(tmpDir, cfg)
	if err != nil {
		t.Fatalf("listGoFiles failed: %v", err)
	}

	foundMap := make(map[string]bool)
	for _, f := range found {
		rel, _ := filepath.Rel(tmpDir, f)
		foundMap[rel] = true
	}

	for name := range wantFound {
		if !foundMap[name] {
			t.Errorf("expected to find %s", name)
		}
	}

	for name := range wantAbsent {
		if foundMap[name] {
			t.Errorf("expected %s to be excluded", name)
		}
	}
}

func TestCheckRepo_NoDuplicates(t *testing.T) {
	tmpDir := t.TempDir()

	file1 := filepath.Join(tmpDir, "file1.go")
	if err := os.WriteFile(file1, []byte(`package main
func funcA() {
	x := 1
	y := 2
}
`), 0644); err != nil {
		t.Fatalf("failed to write file1: %v", err)
	}

	file2 := filepath.Join(tmpDir, "file2.go")
	if err := os.WriteFile(file2, []byte(`package main
func funcB() {
	a := 3
	b := 4
}
`), 0644); err != nil {
		t.Fatalf("failed to write file2: %v", err)
	}

	cfg := DefaultConfig()
	cfg.MinLines = 5
	cfg.MinTokens = 20

	findings, err := CheckRepo(tmpDir, cfg)
	if err != nil {
		t.Fatalf("CheckRepo failed: %v", err)
	}

	if len(findings) != 0 {
		t.Errorf("expected no findings, got %d", len(findings))
	}
}

func TestCheckRepo_WithDuplicates(t *testing.T) {
	tmpDir := t.TempDir()

	// Large code block with enough tokens (MinLines=15, MinTokens=150)
	identicalCode := `package main

import "fmt"

func processData() {
	data := make([]int, 0)
	for i := 0; i < 30; i++ {
		data = append(data, i*2)
		data = append(data, i*3)
	}
	result := 0
	for _, v := range data {
		result += v
	}
	fmt.Println("Processing data:", result)
}

func processMore() {
	data := make([]int, 0)
	for i := 0; i < 30; i++ {
		data = append(data, i*2)
		data = append(data, i*3)
	}
	result := 0
	for _, v := range data {
		result += v
	}
	fmt.Println("Processing more:", result)
}
`

	file1 := filepath.Join(tmpDir, "file1.go")
	if err := os.WriteFile(file1, []byte(identicalCode), 0644); err != nil {
		t.Fatalf("failed to write file1: %v", err)
	}

	file2 := filepath.Join(tmpDir, "file2.go")
	if err := os.WriteFile(file2, []byte(identicalCode), 0644); err != nil {
		t.Fatalf("failed to write file2: %v", err)
	}

	cfg := DefaultConfig()
	cfg.MinLines = 15
	cfg.MinTokens = 150

	findings, err := CheckRepo(tmpDir, cfg)
	if err != nil {
		t.Fatalf("CheckRepo failed: %v", err)
	}

	if len(findings) == 0 {
		t.Fatal("expected to detect duplicates but found none")
	}
}
