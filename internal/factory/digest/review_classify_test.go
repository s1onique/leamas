func TestClassifyFile_DefaultToSource(t *testing.T) {
	tests := []string{
		"main.go",
		"util.go",
		"lib/core.go",
		"handler.ts",
		"renderer.tsx",
		"parser.go",
		"middleware.go",
	}

	for _, path := range tests {
		t.Run(path, func(t *testing.T) {
			result := classifyFile(path)
			if result != "source" {
				t.Errorf("classifyFile(%q) = %q, want %q", path, result, "source")
			}
		})
	}
}

func TestIsGeneratedFileContent(t *testing.T) {
	tests := []struct {
		name     string
		content  string
		expected bool
	}{
		{
			"canonical marker",
			"// Code generated by tool DO NOT EDIT.\npackage main\n",
			true,
		},
		{
			"canonical with different tool",
			"// Code generated by protoc-gen-go DO NOT EDIT.\npackage main\n",
			true,
		},
		{
			"go generate is NOT generated",
			"//go:generate something\npackage main\n",
			false,
		},
		{
			"unrelated code generated text",
			"package main\n// This code generates output\nfunc main() {}\n",
			false,
		},
		{
			"not a comment",
			"package main\n// Code generated by tool DO NOT EDIT.\nfunc main() {}\n",
			false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result := isGeneratedFileContent(tt.content)
			if result != tt.expected {
				t.Errorf("isGeneratedFileContent() = %v, expected %v", result, tt.expected)
			}
		})
	}
}
