func TestClassifyFile_TestPatterns(t *testing.T) {
	tests := []struct {
		path     string
		expected string
	}{
		{"main_test.go", "test"},
		{"my_test.go", "test"},
		{"helper_test.ts", "test"},
		{"util_test.tsx", "test"},
		{"example.test.ts", "test"},
		{"example.test.tsx", "test"},
		{"test_parser.py", "test"},
		{filepath.Join("tests", "helper.go"), "test"},
		{filepath.Join("tests", "integration", "main.go"), "test"},
	}

	for _, tt := range tests {
		t.Run(tt.path, func(t *testing.T) {
			result := classifyFile(tt.path)
			if result != tt.expected {
				t.Errorf("classifyFile(%q) = %q, want %q", tt.path, result, tt.expected)
			}
		})
	}
}

