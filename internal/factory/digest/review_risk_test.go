func TestComputeRiskSignals_LargeFileChanged(t *testing.T) {
	tmpDir := t.TempDir()

	largeFile := filepath.Join(tmpDir, "large.bin")
	largeContent := make([]byte, LargeFileThreshold+1)
	for i := range largeContent {
		largeContent[i] = 'x'
	}
	os.WriteFile(largeFile, largeContent, 0644)

	manifest := []ReviewChangedFile{
		{Status: StatusModified, Path: "large.bin"},
	}

	stats := ComputeStats(manifest, tmpDir)
	rs := ComputeRiskSignals(stats, manifest, tmpDir)

	if !rs.LargeFileChanged {
		t.Error("large_file_changed should be true when a file exceeds threshold")
	}
	if rs.LargeFileThresholdBytes != LargeFileThreshold {
		t.Errorf("large_file_threshold_bytes = %d, want %d", rs.LargeFileThresholdBytes, LargeFileThreshold)
	}
}

func TestRenderRiskSignals_StableKeyOrder(t *testing.T) {
	rs := RiskSignals{
		ProductionWithoutTests:  true,
		TestsWithoutProduction:  false,
		DocsWithoutCode:         false,
		GeneratedFilesChanged:   false,
		ConfigFilesChanged:      true,
		DeletedFilesChanged:     false,
		UnmergedFilesPresent:    false,
		LargeFileChanged:        false,
		LargeFileThresholdBytes: LargeFileThreshold,
	}

	result := RenderRiskSignals(rs)

	expectedOrder := []string{
		"production_without_tests",
		"tests_without_production",
		"docs_without_code",
		"generated_files_changed",
		"config_files_changed",
		"deleted_files_changed",
		"unmerged_files_present",
		"large_file_changed",
		"large_file_threshold_bytes",
	}

	for i, key := range expectedOrder {
		if i > 0 {
			idx := strings.Index(result, key)
			prevIdx := strings.Index(result, expectedOrder[i-1])
			if idx <= prevIdx {
				t.Errorf("key %q should come after %q", key, expectedOrder[i-1])
			}
		}
	}
}

func TestRenderRiskSignals_BooleanValuesLowercase(t *testing.T) {
	rs := RiskSignals{
		ProductionWithoutTests:  true,
		TestsWithoutProduction:  false,
		DocsWithoutCode:         true,
		GeneratedFilesChanged:   false,
		ConfigFilesChanged:      true,
		DeletedFilesChanged:     false,
		UnmergedFilesPresent:    false,
		LargeFileChanged:        true,
		LargeFileThresholdBytes: LargeFileThreshold,
	}

	result := RenderRiskSignals(rs)

	if !strings.Contains(result, "production_without_tests=true") {
		t.Error("expected lowercase true value")
	}
	if !strings.Contains(result, "tests_without_production=false") {
		t.Error("expected lowercase false value")
	}
}
