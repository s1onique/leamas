func TestComputeRiskSignals_ProductionWithoutTests(t *testing.T) {
	tmpDir := t.TempDir()

	sourceFile := filepath.Join(tmpDir, "main.go")
	os.WriteFile(sourceFile, []byte("package main"), 0644)

	manifest := []ReviewChangedFile{
		{Status: StatusModified, Path: "main.go"},
	}

	stats := ComputeStats(manifest, tmpDir)
	rs := ComputeRiskSignals(stats, manifest, tmpDir)

	if !rs.ProductionWithoutTests {
		t.Error("production_without_tests should be true when source changes without tests")
	}
	if rs.TestsWithoutProduction {
		t.Error("tests_without_production should be false")
	}
}

